-- Create SSS Tax Table for 2025 Contribution Rates
-- This contains only range compensation, employer total, employee total and actual total

CREATE TABLE IF NOT EXISTS `sss_tax_table` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `range_start` decimal(10,2) NOT NULL,
  `range_end` decimal(10,2) DEFAULT NULL,
  `employee_share` decimal(10,2) NOT NULL,
  `employer_share` decimal(10,2) NOT NULL,
  `total_contribution` decimal(10,2) NOT NULL,
  `created_at` timestamp NULL DEFAULT NULL,
  `updated_at` timestamp NULL DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `sss_tax_table_range_start_range_end_index` (`range_start`,`range_end`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

